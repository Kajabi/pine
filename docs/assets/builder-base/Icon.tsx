interface IconProps {
  name?: string; // For named icons (web components only - deprecated for React)
  icon?: any; // For imported icon objects (React preferred)
  size?: string | number;
  color?: string;
  className?: string;
  componentId?: string;
}

export const Icon: React.FC<IconProps> = ({
  name,
  icon,
  size,
  color,
  className = '',
  componentId
}) => {
  // Warn about deprecated name prop in React
  React.useEffect(() => {
    if (name && !icon) {
      console.warn('Icon: The "name" prop is deprecated for React. Import the icon and use the "icon" prop instead.');
    }
  }, [name, icon]);

  return (
    <PdsIcon
      name={name}
      icon={icon}
      size={size}
      color={color}
      componentId={componentId}
      className={className}
    />
  );
};
