#!/usr/bin/env node
/**
 * generate-design-md.mjs
 *
 * Regenerates the deterministic token block in DESIGN.md from the upstream
 * source of truth (@kajabi-ui/styles token JSON + Pine-local motion SCSS).
 *
 * The block lives between these markers in DESIGN.md's YAML front matter:
 *   # AUTOGEN:tokens:start ... # AUTOGEN:tokens:end
 * Everything outside the markers (semantic color roles, component mappings,
 * prose) is hand-curated and is NEVER touched by this script.
 *
 * Usage:
 *   node scripts/generate-design-md.mjs           # rewrite the block in place
 *   node scripts/generate-design-md.mjs --check    # CI: exit 1 if block is stale
 *
 * No third-party deps (hand-rolled YAML so it runs on a bare checkout).
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { basename, dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const TOKENS = resolve(ROOT, 'node_modules/@kajabi-ui/styles/dist/tokens');
const MOTION = resolve(ROOT, 'libs/core/src/global/styles/_motion.scss');
const DESIGN = resolve(ROOT, 'DESIGN.md');

const START = '# AUTOGEN:tokens:start';
const END = '# AUTOGEN:tokens:end';

// basename() strips any path segment, so reads are confined to TOKENS — the
// argument is always a literal token filename, never external input.
const readJSON = (f) => JSON.parse(readFileSync(resolve(TOKENS, basename(f)), 'utf8'));
const core = readJSON('core.json');
const semantic = readJSON('semantic.json');

// --- helpers ---------------------------------------------------------------
const norm = (k) => String(parseInt(k, 10)); // "050" -> "50"
const byNum = (a, b) => parseInt(a, 10) - parseInt(b, 10);
const flow = (obj) =>
  '{ ' + Object.entries(obj).map(([k, v]) => `${k}: ${v}`).join(', ') + ' }';

// Resolve a "{a.b.c}" reference against the core token tree -> .value
const deref = (ref) => {
  if (typeof ref !== 'string' || !ref.startsWith('{')) return ref;
  const path = ref.slice(1, -1).split('.');
  let node = core;
  for (const p of path) {
    if (p === '__proto__' || p === 'constructor' || p === 'prototype') return ref;
    node = node?.[p];
  }
  return node?.value ?? ref;
};

const composeShadow = (val) => {
  const layers = Array.isArray(val) ? val : [val];
  return layers
    .map((l) => {
      const parts = [l.x, l.y, l.blur];
      if (l.spread && l.spread !== '0') parts.push(l.spread);
      parts.push(l.color);
      return parts.join(' ');
    })
    .join(', ');
};

// --- read Pine-local motion tokens from SCSS -------------------------------
const motionScss = readFileSync(MOTION, 'utf8');
const motionVar = (name) =>
  motionScss.match(new RegExp(`--pine-motion-${name}:\\s*([^;]+);`))?.[1].trim();

// --- build the block -------------------------------------------------------
const lines = [];
const push = (s = '') => lines.push(s);

push(START);
push('# Generated by scripts/generate-design-md.mjs — do not edit by hand.');
push('# Source: @kajabi-ui/styles core.json + semantic.json, _motion.scss.');
push('tokens:');

// palette (core color ramps)
push('  palette:');
for (const [family, shades] of Object.entries(core.color)) {
  if (shades.value) { push(`    ${family}: "${shades.value}"`); continue; }
  const flat = Object.fromEntries(
    Object.entries(shades)
      .map(([k, v]) => [norm(k), `"${v.value}"`])
      .sort((a, b) => byNum(a[0], b[0]))
  );
  push(`    ${family}: ${flow(flat)}`);
}

// dimension / spacing scale
const dim = Object.fromEntries(
  Object.entries(core.dimension)
    .map(([k, v]) => [norm(k), v.value])
    .sort((a, b) => byNum(a[0], b[0]))
);
push(`  spacing: ${flow(dim)}`);

// border radius (semantic) + width
const radius = Object.fromEntries(
  Object.entries(semantic['border-radius']).map(([k, v]) => [k, deref(v.value)])
);
push(`  border_radius: ${flow(radius)}`);
const bw = Object.fromEntries(
  Object.entries(core['border-width']).map(([k, v]) => [k, v.value])
);
push(`  border_width: ${flow(bw)}`);

// typography
const fs = Object.fromEntries(
  Object.entries(core['font-size'])
    .map(([k, v]) => [k, v.value])
    .sort((a, b) => byNum(a[0], b[0]))
);
push(`  font_size: ${flow(fs)}`);
const fw = Object.fromEntries(
  Object.entries(core['font-weight']).map(([k, v]) => [k, v.value])
);
push(`  font_weight: ${flow(fw)}`);
const lh = Object.fromEntries(
  Object.entries(core['line-height'])
    .map(([k, v]) => [norm(k), v.value])
    .sort((a, b) => byNum(a[0], b[0]))
);
push(`  line_height: ${flow(lh)}`);

// elevation
push('  box_shadow:');
for (const [k, v] of Object.entries(core['box-shadow']).sort((a, b) => byNum(a[0], b[0]))) {
  push(`    "${norm(k)}": "${composeShadow(v.value)}"`);
}

// z-index (semantic names -> resolved value)
const z = Object.fromEntries(
  Object.entries(semantic['z-index'])
    .filter(([k]) => k !== '@')
    .map(([k, v]) => [k, deref(v.value)])
);
push(`  z_index: ${flow(z)}`);

// motion (Pine-local)
push('  motion:');
push(`    duration: ${flow({ fast: motionVar('duration-fast'), base: motionVar('duration-base'), slow: motionVar('duration-slow') })}`);
push(`    easing: { out: "${motionVar('easing-out')}", in: "${motionVar('easing-in')}", in_out: "${motionVar('easing-in-out')}" }`);

// intent (semantic status colors -> hex)
const intent = Object.fromEntries(
  ['danger', 'success', 'warning', 'info'].map((k) => [k, `"${deref(semantic.color[k]['@'].value)}"`])
);
push(`  intent: ${flow(intent)}`);

push(END);
const block = lines.join('\n');

// --- write or check --------------------------------------------------------
const md = readFileSync(DESIGN, 'utf8');
const re = new RegExp(`${START}[\\s\\S]*?${END}`);
if (!re.test(md)) {
  console.error(`✘ Could not find AUTOGEN markers in ${DESIGN}`);
  process.exit(2);
}
const next = md.replace(re, block);

if (process.argv.includes('--check')) {
  if (next !== md) {
    console.error('✘ DESIGN.md token block is stale. Run: npm run design.tokens');
    process.exit(1);
  }
  console.log('✓ DESIGN.md token block is up to date.');
} else {
  writeFileSync(DESIGN, next);
  console.log('✓ Regenerated DESIGN.md token block.');
}
